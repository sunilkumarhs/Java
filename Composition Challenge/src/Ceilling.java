public class Ceilling {
    private float Width;
    private float length;
    private String Color;

    public Ceilling(float width, float length, String color) {
        Width = width;
        this.length = length;
        Color = color;
    }

    public float getWidth() {
        return Width;
    }

    public float getLength() {
        return length;
    }

    public String getColor() {
        return Color;
    }
}
